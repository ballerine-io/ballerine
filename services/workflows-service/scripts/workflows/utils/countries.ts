export interface ICountry {
  fullName: string;
  isoCode: string;
}

export const getCountriesList = (): ICountry[] =>
  Object.entries(isoCountriesRecord).map(([isoCode, fullName]) => ({
    isoCode,
    fullName: Array.isArray(fullName) ? (fullName[0] as string) : (fullName as string),
  }));

export const getCountryStates = (countryCode: string) => {
  if (!countryCode) return [];

  return states.filter(
    state => state.countryCode.toLocaleLowerCase() === countryCode.toLocaleLowerCase(),
  );
};

export const getFullCountryNameByCode = (countryCode: string): string => {
  //@ts-ignore
  const result = isoCountriesRecord[countryCode.toLocaleUpperCase()];

  return Array.isArray(result) ? result[0] : result || '';
};
